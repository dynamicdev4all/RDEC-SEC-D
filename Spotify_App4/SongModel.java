public class SongModel {
    String songName;
    String singerName;
    String songImageUrl;
    String songUrl;

    SongModel(String songName, String singerName, String songImageUrl, String songUrl) {
        this.songName = songName;
        this.singerName = singerName;
        this.songImageUrl = songImageUrl;
        this.songUrl = songUrl;
    }
}
